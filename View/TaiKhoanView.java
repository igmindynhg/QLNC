    private void Display(int i) {
        NguoiDungModel nd = list.get(i);
        txtMaND.setText(nd.getMaNguoiDung());
        txtTaiKhoan.setText(nd.getTaiKhoan());
        txtMatKhau.setText(nd.getMatKhau());
        txtVaiTro.setText(nd.getVaiTro());
        String trangThaiTaiKhoan = nd.getTrangThaiTaiKhoan() ? "1" : "0";
        txtTrangThai.setText(trangThaiTaiKhoan);
    }

