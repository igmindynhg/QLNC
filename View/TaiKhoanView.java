    public final void TaiTrang(ArrayList<NguoiDungModel> arr) {
        tb.getDataVector().removeAllElements();
        for (int i = 0; i < arr.size(); i++) {
            String trangThaiTaiKhoan = arr.get(i).getTrangThaiTaiKhoan() ? "On" : "Off";
            String nd[] = {arr.get(i).getMaNguoiDung(), arr.get(i).getTaiKhoan(), arr.get(i).getMatKhau(), arr.get(i).getVaiTro(), trangThaiTaiKhoan};
            tb.addRow(nd);
//            jTable1.setModel(tb);
        }
        jTable1.setModel(tb);
    }

